# Arithmetic Operators

a = 10
b = 4

# sum
c = a+b
print(c)

# difference
d = a-b
print(d)

# product
e = a*b
print(e)

# division
f = a/b
print(f)

# modulus
g = a%b
print(g)

# exponentiation
h = a**b
print(h)

# floor division
i = a//b
print(i)

